#include "wolf3d.h"

void	ft_config_moves(d_moves *m, t_w *w)
{
	m->posX = w->map.p_x + 1.8;
	m->posY = w->map.p_y + 1.8;
	m->dirX = -1;
	m->dirY = 0;
	m->planeX = 0;
	m->planeY = 0.66;
	m->mv_speed = 0.064;
}
